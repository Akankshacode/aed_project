    private void btninmatesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninmatesActionPerformed

        DefaultCategoryDataset bardataset = new DefaultCategoryDataset();

        bardataset.setValue(210,"FELONS", "2018");
        bardataset.setValue(435,"FELONS", "2019");
        bardataset.setValue(615,"FELONS", "2020");

        JFreeChart bar = ChartFactory.createBarChart3D("Inmates Count for 2018-2020", "YEARS", "INMATES", bardataset,PlotOrientation.VERTICAL, true,true,false);
        CategoryPlot p = (CategoryPlot)bar.getCategoryPlot();
        p.setRangeGridlinePaint(Color.BLUE);
        ChartPanel barPan = new ChartPanel(bar);
        barPanel.removeAll();
        barPanel.add(barPan,BorderLayout.CENTER);
        barPanel.validate();

        DefaultCategoryDataset linedataset = new DefaultCategoryDataset();

        linedataset.setValue(210,"FELONS", "2018");
        linedataset.setValue(435,"FELONS", "2019");
        linedataset.setValue(615,"FELONS", "2020");

        JFreeChart line = ChartFactory.createLineChart("Inmates Count for 2018-2020", "YEARS", "INMATES", linedataset,PlotOrientation.VERTICAL, true,true,false);
        CategoryPlot p1 = (CategoryPlot)line.getCategoryPlot();
        p1.setRangeGridlinePaint(Color.YELLOW);
        ChartPanel linePan = new ChartPanel(line);

        linePanel.removeAll();
        linePanel.add(linePan,BorderLayout.CENTER);
        linePanel.validate();

        DefaultPieDataset piedataset = new DefaultPieDataset();
        
        piedataset.setValue("2018", 210);
        piedataset.setValue("2019", 435);
        piedataset.setValue("2020", 615);

        JFreeChart pieChart = ChartFactory.createPieChart3D("Inmates Count for 2018-2020", piedataset,true,true,true);

        PiePlot3D Pie = (PiePlot3D)pieChart.getPlot();

        ChartPanel piepan = new ChartPanel(pieChart);

        piePanel.removeAll();
        piePanel.add(piepan,BorderLayout.CENTER);
        piePanel.validate();
    }//GEN-LAST:event_btninmatesActionPerformed

